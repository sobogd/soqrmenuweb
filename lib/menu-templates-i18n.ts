export function translateTemplate(
  template: MenuTemplate,
  locale: string,
): MenuTemplate {
  if (locale === "en" || !MENU_I18N[locale]) return template;

  const i18n = MENU_I18N[locale];

  return {
    ...template,
    categories: template.categories.map((cat) => ({
      ...cat,
      name: i18n.c[cat.name] || cat.name,
      items: cat.items.map((item) => ({
        ...item,
        name: i18n.n[item.name] || item.name,
        description: item.description
          ? i18n.d[item.description] || item.description
          : undefined
      }))
    }))
  };
}
